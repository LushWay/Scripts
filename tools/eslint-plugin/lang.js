import fs from 'fs/promises'
import path from 'path'
import { sourceCodeLang, supportedLanguages } from './asset-lang.js'

/** @template T */
class LangWriter {
  /** @type {string} */
  base

  /** @type {(data: Record<string, T>) => string} */
  stringify = data => JSON.stringify(data, null, 2)

  parse = JSON.parse

  /** @type {Record<string, Record<string, T>>} */
  storage = {}

  constructor(base = 'lang') {
    this.base = base
  }

  ext = '.json'

  /** @param {string} lang */
  #path(lang) {
    return path.join(this.base, lang + this.ext)
  }

  readAll() {
    return Promise.all(supportedLanguages.map(e => this.read(e)))
  }

  writeAll() {
    return Promise.all(supportedLanguages.map(e => this.write(e)))
  }

  unusedWarn = new Set()

  /**
   * @param {string} lang
   * @param {Record<string, T>} data
   */
  write(lang, data = { ...this.storage[sourceCodeLang], ...this.storage[lang] }) {
    for (const key in data) {
      const value = data[key]
      if (!(key in this.storage[sourceCodeLang])) {
        if (!this.unusedWarn.has(key)) {
          this.unusedWarn.add(key)
          console.warn(this.prefix, lang, 'Unused key', key)
        }
      } else {
        const expected = this.storage[sourceCodeLang][key]
        if (
          !(
            (typeof value === 'string' && typeof expected === 'string') ||
            (typeof value === 'object' && typeof expected === 'object')
          )
        ) {
          console.warn(
            `${this.prefix}: Unexpected type of message with id ${key} for lang ${lang}`,
            value,
            'expected:',
            expected,
          )
        }
      }
    }
    return fs.writeFile(this.#path(lang), (this.stringify(data) + '\n').replaceAll('\n', '\r\n'), 'utf-8')
  }

  get prefix() {
    return `LangWriter(${this.base}/*.${this.ext})`
  }

  /**
   * @param {string} lang
   * @returns {Promise<Record<string, T>>}
   */
  async read(lang) {
    /** @type {Record<string, T>} */
    let parsed = {}

    try {
      parsed = this.parse(await fs.readFile(this.#path(lang), 'utf-8'))
    } catch (e) {
      console.warn('Unable to read lang', lang, 'with base', this.base, e)
    }

    // 1. Migrate old keys that contain \x00 (the old template separator)
    const migrated = {}
    for (const key in parsed) {
      if (key.includes('\x00')) {
        const newKey = oldKeyToPlaceholderKey(key)
        console.log(`Migrating old key: ${key.replace(/\x00/g, '\\0')}  ->  ${newKey}`)
        migrated[newKey] = parsed[key]
        delete parsed[key]
      }
    }
    Object.assign(parsed, migrated)

    // 2. Convert old array values to placeholder strings
    for (const key in parsed) {
      const val = parsed[key]
      if (Array.isArray(val)) {
        parsed[key] = arrayToPlaceholderString(val)
        console.log(`Migrated array to string for ${lang}: ${key}`)
      } else if (typeof val === 'object' && val !== null) {
        // Plural object with possible array values
        for (const rule in val) {
          if (Array.isArray(val[rule])) {
            val[rule] = arrayToPlaceholderString(val[rule])
          }
        }
      }
    }

    if (lang === sourceCodeLang) {
      this.storage[lang] = parsed
    } else {
      this.storage[lang] = {
        ...parsed,
        ...(this.storage[lang] ?? {}),
      }
    }

    console.log(`${this.prefix}:`, 'Read', lang, 'with keys', Object.keys(parsed).length)

    return parsed
  }
}

/** Convert an old key like "Событие! \x00 силой \x00 на \x00 минут" into "Событие! {0} силой {1} на {2} минут" */
function oldKeyToPlaceholderKey(key) {
  const parts = key.split('\x00')
  let result = ''
  for (let i = 0; i < parts.length; i++) {
    result += parts[i]
    if (i < parts.length - 1) {
      result += `{${i}}`
    }
  }
  return result
}

/** Convert old array value ["part1", "part2", ""] to "part1{0}part2{1}" */
function arrayToPlaceholderString(arr) {
  let result = ''
  for (let i = 0; i < arr.length; i++) {
    result += arr[i]
    if (i < arr.length - 1) result += `{${i}}`
  }
  return result
}

/** @typedef {string | Record<string, string>} Message */
/** @type {LangWriter<Message>} */
export const messagesJson = new LangWriter('lang')

/** @type {LangWriter<string>} */
export const sharedMessages = new LangWriter('texts')
sharedMessages.ext = '.lang'
sharedMessages.stringify = data => {
  let t = '### This file is autogenerated, do not edit\n\n'
  for (const k in data) t += `${k}=${data[k].replaceAll('\\n', '~LINEBREAK~')}\n`
  return t
}
sharedMessages.parse = () => ({})

export async function readMessages() {
  await sharedMessages.readAll()
  await messagesJson.readAll()
}

/**
 * Clean unused translations when CLEAN=1. Merges translations from old unlabeled keys into the new labeled keys, then
 * removes any key not present in the final source language.
 */
function cleanTranslations() {
  // First, deduplicate and merge across all languages
  deduplicateAndMergeAllLanguages()

  const source = messagesJson.storage[sourceCodeLang]
  for (const lang of supportedLanguages) {
    if (lang === sourceCodeLang) continue
    const target = messagesJson.storage[lang]
    for (const key in target) {
      if (!(key in source)) {
        delete target[key]
        console.log(`Removed unused key ${key} from ${lang}`)
      }
    }
  }
}

/**
 * For each group of keys that normalize to the same string (ignoring labels), select the canonical key (prefer labeled)
 * for each language. In non‑source languages, if a canonical key still has the default value (its own key), copy the
 * translation from an unlabeled key that has a real translation. Then remove all non‑canonical keys from all
 * languages.
 */
function deduplicateAndMergeAllLanguages() {
  const placeholderRegex = /\{(\d+)(?:_\w+)?\}/g
  const normalize = key => key.replace(placeholderRegex, (_, index) => `{${index}}`)

  /** @type {Record<string, string[]>} */ // normalized -> list of actual keys across all languages
  const allGroups = {}

  // Collect keys from all languages
  for (const lang of supportedLanguages) {
    const data = messagesJson.storage[lang]
    for (const key in data) {
      const norm = normalize(key)
      if (!allGroups[norm]) allGroups[norm] = []
      if (!allGroups[norm].includes(key)) allGroups[norm].push(key)
    }
  }

  // For each group, decide the canonical key
  /** @type {Record<string, string>} */ // normalized -> canonical key
  const canonicalKeyMap = {}
  /** @type {Record<string, Record<string, string>>} */ // normalized -> { lang: canonical key }
  const languageCanonicalMap = {}

  for (const norm in allGroups) {
    const keys = allGroups[norm]

    // Prefer labeled keys (contain '_' in any placeholder)
    const labeled = keys.filter(k => /_\w+\}/.test(k))
    const unlabeled = keys.filter(k => !/_\w+\}/.test(k))

    let canonicalKey = null
    if (labeled.length > 0) {
      canonicalKey = labeled[0]
    } else if (unlabeled.length > 0) {
      canonicalKey = unlabeled[0]
    }

    if (!canonicalKey) continue

    canonicalKeyMap[norm] = canonicalKey

    // For each language, pick the canonical key that exists there, if any.
    // If the language has the canonical key, use it; else, if it has another key from the group,
    // we'll later rename that key to canonical? No, we'll just merge translations.
    // We'll handle per language later.
    languageCanonicalMap[norm] = {}
    for (const lang of supportedLanguages) {
      const langData = messagesJson.storage[lang]
      const presentKeys = keys.filter(k => k in langData)
      if (presentKeys.length === 0) {
        languageCanonicalMap[norm][lang] = canonicalKey // will be absent, but keep reference
      } else {
        // Use the canonical key if present, else the first present key (prefer labeled)
        const canonicalHere = presentKeys.find(k => k === canonicalKey) || presentKeys[0]
        languageCanonicalMap[norm][lang] = canonicalHere
      }
    }
  }

  // Now merge translations: for each language and each group, if the canonical key
  // exists and has the default value (equals its own key) and there is another key
  // with a different value, copy that value to the canonical key.
  for (const lang of supportedLanguages) {
    const langData = messagesJson.storage[lang]
    for (const norm in canonicalKeyMap) {
      const canonical = languageCanonicalMap[norm][lang]
      if (!(canonical in langData)) continue

      const canonicalVal = langData[canonical]
      const isDefault =
        (typeof canonicalVal === 'string' && canonicalVal === canonical) ||
        (typeof canonicalVal === 'object' &&
          canonicalVal !== null &&
          JSON.stringify(canonicalVal) === JSON.stringify(defaultPluralForCanonical(canonical)))

      if (!isDefault) continue // already translated, don't overwrite

      // Look for other keys in this language that belong to the group and have a non‑default translation
      const otherKeys = allGroups[norm].filter(k => k in langData && k !== canonical)
      for (const other of otherKeys) {
        const otherVal = langData[other]
        const otherIsDefault =
          (typeof otherVal === 'string' && otherVal === other) ||
          (typeof otherVal === 'object' &&
            otherVal !== null &&
            JSON.stringify(otherVal) === JSON.stringify(defaultPluralForCanonical(other)))

        if (!otherIsDefault) {
          // Copy translation
          if (typeof otherVal === 'string') {
            langData[canonical] = otherVal
            console.log(`[${lang}] Copied translation from "${other}" to "${canonical}"`)
          } else if (typeof otherVal === 'object' && otherVal !== null) {
            // plural object
            langData[canonical] = { ...otherVal }
            console.log(`[${lang}] Copied plural translation from "${other}" to "${canonical}"`)
          }
          break // use the first non‑default we find
        }
      }
    }
  }

  // Remove all non‑canonical keys from all languages
  for (const lang of supportedLanguages) {
    const langData = messagesJson.storage[lang]
    for (const key in langData) {
      const norm = normalize(key)
      if (norm in canonicalKeyMap && key !== languageCanonicalMap[norm][lang]) {
        delete langData[key]
        console.log(`[${lang}] Removed duplicate key "${key}"`)
      }
    }
  }
}

/**
 * Returns the default plural object for a given canonical key. Since the default is simply the key itself repeated for
 * each plural form, we can generate it from the plurals list.
 */
function defaultPluralForCanonical(key) {
  const rules = plurals[sourceCodeLang] // use source plurals as template
  const obj = {}
  for (const rule of rules) {
    obj[rule] = key
  }
  return obj
}

/** Pre‑process a placeholder string into { s: string[], i: number[] } */
function preprocessPlaceholderString(str) {
  /** @type {string[]} */
  const segments = []
  /** @type {number[]} */
  const indices = []
  const regex = /\{(\d+)(?:_\w+)?\}/g
  let lastIndex = 0
  let match
  while ((match = regex.exec(str)) !== null) {
    segments.push(str.slice(lastIndex, match.index))
    indices.push(parseInt(match[1], 10))
    lastIndex = match.index + match[0].length
  }
  segments.push(str.slice(lastIndex))
  return { s: segments, i: indices }
}

export async function writeMessages() {
  if (process.env.CLEAN === '1') {
    cleanTranslations()
  }

  await sharedMessages.writeAll()
  await messagesJson.writeAll()

  // Build pre‑processed output for simple messages and plurals
  /** @type {Record<string, Record<string, { s: string[]; i: number[] }>>} */
  const messagesOut = {}
  /** @type {Record<string, Record<string, Record<string, { s: string[]; i: number[] }>>>} */
  const pluralsOut = {}

  for (const lang of supportedLanguages) {
    const langData = messagesJson.storage[lang]
    /** @type {Record<string, { s: string[]; i: number[] }>} */
    const simple = {}
    /** @type {Record<string, Record<string, { s: string[]; i: number[] }>>} */
    const plural = {}

    for (const id in langData) {
      const value = langData[id]
      if (typeof value === 'string') {
        simple[id] = preprocessPlaceholderString(value)
      } else if (typeof value === 'object' && !Array.isArray(value)) {
        /** @type {Record<string, { s: string[]; i: number[] }>} */
        const forms = {}
        for (const rule in value) {
          forms[rule] = preprocessPlaceholderString(value[rule])
        }
        plural[id] = forms
      }
    }

    messagesOut[lang] = simple
    pluralsOut[lang] = plural
  }

  const sharedIds = Object.fromEntries(
    Object.entries(sharedMessages.storage[sourceCodeLang]).map(([id, token]) => [id, templateToSharedId(id)]),
  )

  await fs.writeFile(
    path.join('src/lib/assets', 'lang-messages.ts'),
    `
/* eslint-ignore */
/* i18n-ignore */
// Autogenerated by translate plugin

type Language = string
type MessageId = string

export const extractedSharedMessagesIds: Record<MessageId, string> = ${JSON.stringify(sharedIds, null, 2)}

/**
 * Preprocessed simple messages.
 * Structure: { s: string[], i: number[] }
 */
export const extractedTranslatedMessages: Record<Language, Record<MessageId, { s: string[], i: number[] }>> = ${JSON.stringify(messagesOut, null, 2)}

/**
 * Preprocessed plural messages.
 * Structure: Record<PluralRule, { s: string[], i: number[] }>
 */
export const extractedTranslatedPlurals: Record<Language, Record<MessageId, Record<string, { s: string[], i: number[] }>>> = ${JSON.stringify(pluralsOut, null, 2)}
`.replaceAll('\\\\n', '\\n'),
  )
}

/** @param {string} id */
function templateToSharedId(id) {
  return (
    'script.shared.' +
    id
      .toLowerCase()
      .replace(/§./g, '')
      .replace(/\{(\d+)(?:_\w+)?\}/g, '_$1_')
      .replace(/[^a-zA-Zа-яА-Я.$_0-9]/g, '_')
      .replace(/\.+/g, '.')
      .replace(/_+/g, '_')
  )
}

/** @type {Record<string, string[]>} */
const plurals = Object.fromEntries(
  supportedLanguages.map(e => [e, new Intl.PluralRules(e.replace('_', '-')).resolvedOptions().pluralCategories]),
)

/**
 * Adds or updates a translation entry.
 *
 * @param {string} id - The message ID with placeholders, e.g. "Вы добыли {0_amount} из {1_needed}"
 * @param {boolean} shared
 * @param {boolean} plural
 */
export function addTranslation(id, shared, plural) {
  const sourcePrev = messagesJson.storage[sourceCodeLang][id]

  if (plural) {
    const sourceCategories = plurals[sourceCodeLang]
    const defaultPlurals = Object.fromEntries(sourceCategories.map(c => [c, id]))
    messagesJson.storage[sourceCodeLang][id] = {
      ...defaultPlurals,
      ...(sourcePrev && typeof sourcePrev === 'object' && !Array.isArray(sourcePrev) ? sourcePrev : {}),
    }
  } else {
    messagesJson.storage[sourceCodeLang][id] = id
  }

  for (const lang of supportedLanguages) {
    if (lang === sourceCodeLang) continue
    const prev = messagesJson.storage[lang][id]
    if (plural) {
      if (prev === undefined || (typeof prev === 'object' && !Array.isArray(prev))) {
        const langCategories = plurals[lang]
        const defaultPlurals = Object.fromEntries(langCategories.map(c => [c, id]))
        messagesJson.storage[lang][id] = {
          ...defaultPlurals,
          ...(typeof prev === 'object' ? prev : {}),
        }
      } else {
        throw new Error(`Plural key conflict for ${id} in ${lang}`)
      }
    } else {
      if (prev === undefined) messagesJson.storage[lang][id] = id
    }
  }

  if (shared || plural) {
    for (const lang of supportedLanguages) {
      const translation = messagesJson.storage[lang][id]
      if (typeof translation === 'string') {
        sharedMessages.storage[lang][id] = translation.replace(/\{(\d+)(?:_\w+)?\}/g, '%s')
      } else if (translation && typeof translation === 'object') {
        for (const rule in translation) {
          sharedMessages.storage[lang][`${id}.${rule}`] = translation[rule].replace(/\{(\d+)(?:_\w+)?\}/g, '%s')
        }
      }
    }
  }
}
